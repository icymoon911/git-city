export const PF_HEIGHT = 5;

/** Concatenate per-letter bitmaps with 1-column spacing. */
export function makeBitmap(word: string): number[][] {
  const upper = word.toUpperCase();
  const letters: number[][][] = [];
  for (const ch of upper) {
    const g = PF[ch];
    if (g) letters.push(g);
  }
  if (letters.length === 0) return Array.from({ length: PF_HEIGHT }, () => [0]);

  const h = letters[0].length;
  let w = 0;
  for (let i = 0; i < letters.length; i++) {
    w += letters[i][0].length;
    if (i < letters.length - 1) w++;
  }
  const bm = Array.from({ length: h }, () => Array(w).fill(0));
  let col = 0;
  for (const L of letters) {
    for (let r = 0; r < h; r++) {
      for (let c = 0; c < L[0].length; c++) {
        bm[r][col + c] = L[r][c];
      }
    }
    col += L[0].length + 1;
  }
  return bm;
}

